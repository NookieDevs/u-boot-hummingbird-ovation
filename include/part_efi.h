#ifndef _PART_EFI_H
#define _PART_EFI_H

#define EFI_VERSION 0x00010000
#define EFI_ENTRIES 128
#define EFI_NAMELEN 36

struct efi_entry {
	u8 type_uuid[16];
	u8 uniq_uuid[16];
	u64 first_lba;
	u64 last_lba;
	u64 attr;
	u16 name[EFI_NAMELEN];
};

struct efi_header {
	u8 magic[8];

	u32 version;
	u32 header_sz;

	u32 crc32;
	u32 reserved;

	u64 header_lba;
	u64 backup_lba;
	u64 first_lba;
	u64 last_lba;

	u8 volume_uuid[16];

	u64 entries_lba;

	u32 entries_count;
	u32 entries_size;
	u32 entries_crc32;
} __attribute__((packed));

struct efi_ptable {
	u8 mbr[512];
	union {
		struct efi_header header;
		u8 block[512];
	};
	struct efi_entry entry[EFI_ENTRIES];
};

struct efi_partition_info {
	const char *name;
	unsigned start_kb; /* partition offset. 0 means continue from previous */
	unsigned size_kb;  /* partition size.   0 means autosize to the end of media */
};

int   efi_load_ptbl(int dev);
int   efi_do_format(struct efi_partition_info *partitions, int dev);
char* efi_get_partition_sz(char *buf, const char *partname, int dev);

#endif	/* _PART_EFI_H */
